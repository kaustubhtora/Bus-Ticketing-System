     public void MyBookingData(){
    
         try{
           Class.forName("com.mysql.jdbc.Driver");
            String databaseURL="jdbc:mysql://localhost:3306/busm";
            Connection con=DriverManager.getConnection(databaseURL,"root","");
            Statement stat =con.createStatement();
            String selectQuery="select * from booking_details ";
            ResultSet rs=stat.executeQuery(selectQuery);  
    
            while(rs.next())
            {
            String firstname=rs.getString("firstname");
            String lastname=rs.getString("lastname");
            String Source=rs.getString("Source");
            String Destination=rs.getString("Destination");
           String startingdate=rs.getString("starting date");
           String upto=rs.getString("upto");
           String busno=rs.getString("bus no");
           String seat=rs.getString("seat");
           
           String tbData[] = {firstname,lastname,Source,Destination,startingdate,upto,seat,busno};
           DefaultTableModel tblModel=(DefaultTableModel)jTable1.getModel();
            jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            tblModel.addRow(tbData);
            
            }
            
        }
        catch(Exception e){
        System.out.println(e);
        }
    } 
